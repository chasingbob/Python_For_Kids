# Assigning the int value 10 to the variable age
age = 10
# Assign the string 'John' to name - a string/text should be between quotes '...'
name = 'John'

# {} is used as placeholders, and format replaces the {} placeholders with the variables supplied in the same order
print('{} is {} years old'.format(name, age))

